#include "3-calc.h"
/**
* op_add - funtion return sum
*
* @a: first number
*
* @b: second number
*
* Return: int
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - funtion return sub
*
* @a: first number
*
* @b: second number
*
* Return: int
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - funtion return mul
*
* @a: first number
*
* @b: second number
*
* Return: int
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - funtion return div
*
* @a: first number
*
* @b: second number
*
* Return: int
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - funtion return mod
*
* @a: first number
*
* @b: second number
*
* Return: int
*/

int op_mod(int a, int b)
{
	return (a % b);
}
